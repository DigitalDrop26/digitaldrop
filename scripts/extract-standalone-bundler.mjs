/**
 * One-off extractor for Claude Design "__bundler" standalone HTML exports.
 */
import fs from "fs";
import path from "path";
import zlib from "zlib";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PROJECT = path.resolve(__dirname, "..");
const DEFAULT_INPUT = "/Users/michelesalaris/Downloads/Drop Homepage - Standalone (3).html";
const INPUT = process.argv[2] || DEFAULT_INPUT;
const OUT = path.join(PROJECT, "extracted-bundle");

function extractScript(html, type) {
  const escaped = type.replace(/\//g, "\\/");
  const re = new RegExp(`<script type="${escaped}">\\s*([\\s\\S]*?)\\s*<\\/script>`, "m");
  const m = html.match(re);
  if (!m) throw new Error(`Missing script type="${type}"`);
  return m[1].trim();
}

function mimeToExt(mime) {
  const map = {
    "image/jpeg": ".jpg",
    "image/png": ".png",
    "image/svg+xml": ".svg",
    "image/webp": ".webp",
    "font/ttf": ".ttf",
    "font/woff2": ".woff2",
    "application/javascript": ".js",
    "text/javascript": ".js",
  };
  return map[mime] || `.bin`;
}

function main() {
  if (!fs.existsSync(INPUT)) {
    console.error("Input not found:", INPUT);
    process.exit(1);
  }
  const html = fs.readFileSync(INPUT, "utf8");
  console.log("Read standalone, bytes:", html.length);

  fs.mkdirSync(path.join(OUT, "assets"), { recursive: true });

  const manifestRaw = extractScript(html, "__bundler/manifest");
  console.log("Manifest JSON chars:", manifestRaw.length);
  const manifest = JSON.parse(manifestRaw);
  let written = 0;
  for (const [id, entry] of Object.entries(manifest)) {
    if (!entry || typeof entry !== "object") continue;
    const { mime, data, compressed } = entry;
    if (!data || typeof data !== "string") continue;
    let buf = Buffer.from(data, "base64");
    if (compressed) {
      buf = zlib.gunzipSync(buf);
    }
    const ext = mimeToExt(mime || "application/octet-stream");
    const dest = path.join(OUT, "assets", `${id}${ext}`);
    fs.writeFileSync(dest, buf);
    written++;
    if (written <= 8) console.log("  wrote", id + ext, buf.length);
  }
  console.log("Wrote asset files:", written);

  let templateEscaped = extractScript(html, "__bundler/template");
  if (templateEscaped.startsWith('"') && templateEscaped.endsWith('"')) {
    templateEscaped = JSON.parse(templateEscaped);
  }
  fs.writeFileSync(path.join(OUT, "decoded-template.html"), templateEscaped, "utf8");
  console.log("Decoded template chars:", templateEscaped.length);

  try {
    const extRaw = extractScript(html, "__bundler/ext_resources");
    fs.writeFileSync(path.join(OUT, "ext_resources.json"), extRaw);
    console.log("ext_resources.json chars:", extRaw.length);
  } catch {
    console.log("(no __bundler/ext_resources)");
  }

  fs.writeFileSync(
    path.join(OUT, "README.txt"),
    "Generated by scripts/extract-standalone-bundler.mjs.\n",
  );
}

main();
